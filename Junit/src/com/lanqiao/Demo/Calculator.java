package com.lanqiao.Demo;

public class Calculator {
    
    public double add(double a,int b) {
        return a + b + 1; 
    }
    
    public int substract(int a,int b) {
        return a - b; 
    }
    
    public int cheng(int a,int b) {
        return a * b;
    }
    
    public int chu(int a,int b) {
        return a/b;
    }

}